# Variables are used for storing data values.
# Rules while creating variables's name:
# 1. A variabl name must be start with a letter or the underscore character.
# 2. A variable name cannot start with a number.
# 3. A variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _).
# 4. Variable names are case-sensitive (age, Age, AGE are three different variables).
# 5. A variable name cannot be any of the Python keywords.

# Example
x = 5
y = -2
z = "Hello World!"

name = "Ashim Babu Shrestha"
age = 25
print("My name is ", name)
print("My age is", age)
